#include "libft.h"

int	ft_atoi(const char *str)
{
	int	k;
	int	n;

	k = 1;
	n = 0;
	if (*str == '\0')
		return (0);
	while ((*str >= 9 && *str <= 13) || *str == 32)
		str++;
	if ((*(str + 1) >= 48 && *(str + 1) <= 57))
	{
		if (*str == 45)
		{
			k = -1;
			str++;
		}
		if (*str == 43)
			str++;
	}
	while (*str >= 48 && *str <= 57 && *str != '\0')
	{
		n = n * 10 + (*str - '0');
		str++;
	}
	return (n * k);
}
